    public IList<string> GenerateParenthesis(int n)
    {
        List<string> result = new List<string>();
        Search(n, n, new StringBuilder(), result);

        return result;
    }

    private void Search(int leftRemains, int rightRemains, StringBuilder currentAnswer, List<string> res)
    {
        if(leftRemains == 0 && rightRemains == 0)
        {
            res.Add(currentAnswer.ToString());
            return;
        }

        if(leftRemains > 0)
        {
            Search(leftRemains - 1, rightRemains, currentAnswer.Append("("), res);
            currentAnswer.Length--;
        }

        if(rightRemains > 0 && rightRemains > leftRemains)
        {
            Search(leftRemains, rightRemains - 1, currentAnswer.Append(")"), res);
            currentAnswer.Length--;
        }
    }

